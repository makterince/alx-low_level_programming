#include "3-calc.h"
/**
 *op_add - adds a and b
 *@a: parameter
 *@b: parameter
 *Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - minus a from b
 *@a: parameter
 *@b: parameter
 *Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - times a and b
 *@a: parameter
 *@b: parameter
 *Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - divides a and b
 *@a: parameter
 *@b: parameter
 *Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - gets mod of a and b
 *@a: parameter
 *@b: parameter
 *Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

